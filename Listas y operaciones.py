# Crear una lista
numeros = [1, 2, 3, 4, 5]

# Agregar un elemento
numeros.append(6)

# Eliminar un elemento
numeros.remove(3)

# Acceder a un elemento por índice
print(numeros[0])  # Primer elemento
print(numeros[-1])  # Último elemento

# Recorrer la lista
for numero in numeros:
    print(numero)
